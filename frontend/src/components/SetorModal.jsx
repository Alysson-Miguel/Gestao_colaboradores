import { useState, useEffect } from "react";
import { Button } from "../components/UIComponents";
import { X } from "lucide-react";

export default function SetorModal({ setor, onClose, onSave }) {
  const [form, setForm] = useState({
    nomeSetor: setor?.nomeSetor || "",
    descricao: setor?.descricao || "",
    ativo: setor?.ativo ?? true,
  });

  /* üîí Bloqueia scroll do body */
  useEffect(() => {
    document.body.style.overflow = "hidden";
    return () => {
      document.body.style.overflow = "auto";
    };
  }, []);

  const handle = (field, value) =>
    setForm((prev) => ({ ...prev, [field]: value }));

  return (
    <div className="fixed inset-0 z-50 flex items-end sm:items-center justify-center px-4 sm:px-6">

      {/* Overlay */}
      <div
        className="absolute inset-0 bg-black/70 backdrop-blur-sm"
        onClick={onClose}
      />

      {/* Modal */}
      <div
        className="
          relative
          w-full
          max-w-lg
          max-h-[92vh]
          bg-[#1A1A1C]
          border border-[#3D3D40]
          rounded-t-2xl sm:rounded-xl
          shadow-2xl
          flex flex-col
        "
      >
        {/* HEADER */}
        <div className="flex items-center justify-between px-4 sm:px-6 py-4 border-b border-[#3D3D40]">
          <h2 className="text-base sm:text-lg font-semibold text-white">
            {setor ? "Editar Setor" : "Novo Setor"}
          </h2>

          <button
            onClick={onClose}
            className="p-2 rounded-md hover:bg-[#2A2A2C] text-[#BFBFC3]"
          >
            <X size={18} />
          </button>
        </div>

        {/* CONTENT (scroll√°vel) */}
        <div className="flex-1 overflow-y-auto px-4 sm:px-6 py-4 space-y-4">

          <div>
            <label className="block text-xs text-[#BFBFC3] mb-1">
              Nome do Setor
            </label>
            <input
              value={form.nomeSetor}
              onChange={(e) => handle("nomeSetor", e.target.value)}
              className="
                w-full px-3 sm:px-4 py-2.5
                bg-[#2A2A2C]
                border border-[#3D3D40]
                rounded-xl
                text-white text-sm
                focus:outline-none focus:ring-1 focus:ring-[#FA4C00]
              "
            />
          </div>

          <div>
            <label className="block text-xs text-[#BFBFC3] mb-1">
              Descri√ß√£o
            </label>
            <textarea
              rows={3}
              value={form.descricao}
              onChange={(e) => handle("descricao", e.target.value)}
              className="
                w-full px-3 sm:px-4 py-2.5
                bg-[#2A2A2C]
                border border-[#3D3D40]
                rounded-xl
                text-white text-sm
                focus:outline-none focus:ring-1 focus:ring-[#FA4C00]
              "
            />
          </div>

          <div>
            <label className="block text-xs text-[#BFBFC3] mb-1">
              Status
            </label>
            <select
              value={form.ativo ? "true" : "false"}
              onChange={(e) => handle("ativo", e.target.value === "true")}
              className="
                w-full px-3 sm:px-4 py-2.5
                bg-[#2A2A2C]
                border border-[#3D3D40]
                rounded-xl
                text-white text-sm
              "
            >
              <option value="true">Ativo</option>
              <option value="false">Inativo</option>
            </select>
          </div>

        </div>

        {/* FOOTER */}
        <div className="flex flex-col sm:flex-row justify-end gap-3 px-4 sm:px-6 py-4 border-t border-[#3D3D40]">
          <Button.Secondary
            onClick={onClose}
            className="w-full sm:w-auto"
          >
            Cancelar
          </Button.Secondary>

          <Button.Primary
            onClick={() => onSave(form)}
            className="w-full sm:w-auto"
          >
            {setor ? "Salvar altera√ß√µes" : "Criar setor"}
          </Button.Primary>
        </div>
      </div>
    </div>
  );
}